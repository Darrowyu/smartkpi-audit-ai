// 简易 Avatar 组件 (旧版兼容，用于 Sidebar 等场景)
interface SimpleAvatarProps {
  name: string;
  email?: string;
  size?: 'sm' | 'md' | 'lg';
  className?: string;
}

const sizeClasses = {
  sm: 'h-8 w-8 text-xs',
  md: 'h-10 w-10 text-sm',
  lg: 'h-12 w-12 text-base',
};

function getInitials(name: string): string {
  return name
    .split(' ')
    .map(n => n[0])
    .join('')
    .toUpperCase()
    .slice(0, 2);
}

function getColorFromEmail(email: string): string {
  const colors = [
    'bg-red-500', 'bg-orange-500', 'bg-amber-500', 'bg-yellow-500',
    'bg-lime-500', 'bg-green-500', 'bg-emerald-500', 'bg-teal-500',
    'bg-cyan-500', 'bg-sky-500', 'bg-blue-500', 'bg-indigo-500',
    'bg-violet-500', 'bg-purple-500', 'bg-fuchsia-500', 'bg-pink-500',
  ];
  let hash = 0;
  for (let i = 0; i < email.length; i++) {
    hash = email.charCodeAt(i) + ((hash << 5) - hash);
  }
  return colors[Math.abs(hash) % colors.length];
}

const SimpleAvatar: React.FC<SimpleAvatarProps & React.HTMLAttributes<HTMLDivElement>> = ({
  name = 'User',
  email = '',
  size = 'md',
  className,
  children,
  ...restProps
}) => {
  const initials = name ? getInitials(name) : 'U';
  const colorClass = getColorFromEmail(email);

  return (
    <div
      className={cn(
        'relative flex shrink-0 overflow-hidden rounded-full items-center justify-center text-white font-medium',
        sizeClasses[size] || sizeClasses.md,
        colorClass,
        className
      )}
      {...restProps}
    >
      {children || initials}
    </div>
  );
};

export { Avatar, AvatarImage, AvatarFallback, SimpleAvatar };
